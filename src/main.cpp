#include <iostream>
#include <raylib-cpp.hpp>

#include "game/game_state.hpp"

int main() {
    // Initialization
    //--------------------------------------------------------------------------------------
    int screenWidth = 1920;
    int screenHeight = 1080;

    raylib::Color textColor(BLACK);
    raylib::Window w(screenWidth, screenHeight, "Dino");
    w.ToggleFullscreen();

    // Test Ground
    //--------------------------------------------------------------------------------------
    GameState game(true,
                   raylib::Vector2((float)screenWidth, (float)screenHeight));
    //--------------------------------------------------------------------------------------

    // Frame Rate
    //--------------------------------------------------------------------------------------
    SetTargetFPS(60);

    // Main game loop
    //--------------------------------------------------------------------------------------
    while (!w.ShouldClose()) {
        // Update
        //--------------------------------------------------------------------------------------
        game.update(w.GetFrameTime());
        //--------------------------------------------------------------------------------------

        // Draw
        //--------------------------------------------------------------------------------------
        BeginDrawing();

        ClearBackground(WHITE);
        // textColor.DrawText("THE DINO WILL RISE!", 190, 200, 20);

        game.draw();
        EndDrawing();
        //--------------------------------------------------------------------------------------
    }
    //--------------------------------------------------------------------------------------

    // End
    //--------------------------------------------------------------------------------------
    w.Close();
    return 0;
}
